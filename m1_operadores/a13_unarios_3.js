console.log(!true);
console.log(!!false);

console.log(!!'Texto');
console.log(!"");

console.log(!!1);
console.log(!!0);
console.log(!!-1);
console.log(!!-0.00001);

/*
    O ! significa não contudo, caso você
    coloque !! ele mantém o estado dele.
    Logo, se eu colocar !true ele será 
    false e caso eu coloque !!true ele se
    manterá true.
*/